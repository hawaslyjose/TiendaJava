    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int clic_tabla = jTable1.rowAtPoint(evt.getPoint());

        String i=""+jTable1.getValueAt(clic_tabla, 0);
        String al = ""+jTable1.getValueAt(clic_tabla, 1);
        String fa = ""+jTable1.getValueAt(clic_tabla, 2);
        String fe = ""+jTable1.getValueAt(clic_tabla, 3);

        jTextField1.setText(i);
        jTextField2.setText(al);
        jTextField3.setText(fa);
        jTextField4.setText(fe);

        int column = jTable1.getColumnModel().getColumnIndexAtX(evt.getX());
        int row = evt.getY()/jTable1.getRowHeight();

        if(row < jTable1.getRowCount() && row >= 0 && column < jTable1.getColumnCount() && column >= 0){

            Object value = jTable1.getValueAt(row, column);
            if(value instanceof JButton){
                ((JButton)value).doClick();
                JButton boton = (JButton) value;

                if(boton.getName().equals("m")){
                    jButton2.setEnabled(true);
                    System.out.println("Click en el boton modificar");
                    //Evento modificar
                }
                if(boton.getName().equals("e")){
                    jButton3.setEnabled(true);
                    System.out.println("Click en el boton eliminar");
                    //Evento eliminar
                }
            }

        }

    }//GEN-LAST:event_jTable1MouseClicked

